package com.jhs.open.bean;

/**
 * 矩形矢量
 * 
 * @author JHS
 * 
 */
public class Rect2D {

	public int x;

	public int y;

	public int w;

	public int h;

	public Rect2D() {

	}

	public Rect2D(int x, int y, int w, int h) {
		super();
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
	}

}
